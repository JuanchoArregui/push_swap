#include "../libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*end;

	if (lst == NULL || new == NULL)
		return ;
	if (*lst)
	{
		end = *lst;
		while (end->next)
			end = end->next;
		end->next = new;
	}
	else
		*lst = new;
}
